    private void CheckEmpty(){
        
        //NOT COMPLETE
        if(txtPaymentID.getText().equals("")){
           IsEmpty = true;
           defaultLabelColor();
           lblPaymentID.setForeground(Color.red);
        }else if(txtPaymentAmount.getText().equals("")){
           IsEmpty = true;
           defaultLabelColor();
           lblPaymentAmount.setForeground(Color.red);
        }else if(txtManagerID.getText().equals("")){
           IsEmpty = true;
           defaultLabelColor();
           lblManagerID.setForeground(Color.red);
        }else if(txtTechnicianID.getText().equals("")){
           IsEmpty = true;
           defaultLabelColor();
           lblTechnicianID.setForeground(Color.red);
        }else if(txtAppointmentID.getText().equals("")){
           IsEmpty = true;
           defaultLabelColor();
           lblAppointmentID.setForeground(Color.red);
        }else {
            IsEmpty = false;
           defaultLabelColor();
        }
       
    }
    
