    private void txtPaymentIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPaymentIDKeyReleased
        String value = txtPaymentID.getText();
        int l = value.length();
        if ((evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') || evt.getKeyChar() <= '-' ) {
            if(l <0 || l>12){
                JOptionPane.showMessageDialog(null, "Invalid Length");
                txtPaymentID.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please enter only numbers");
            txtPaymentID.setText("");
        }
    }//GEN-LAST:event_txtPaymentIDKeyReleased

